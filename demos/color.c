#include <stdio.h>
#include <stdlib.h>

#include "du.h"

/************************************************************************************************************/
/************************************************************************************************************/
/************************************************************************************************************/

static void _print_cl(const char *str);

/************************************************************************************************************/
/************************************************************************************************************/
/************************************************************************************************************/

int
main(void)
{
	_print_cl("#ffffff");
	_print_cl("00000080");
	_print_cl("#aaa");
	_print_cl("#A0A0WA");
	_print_cl("000000");

	return 0;
}

/************************************************************************************************************/
/************************************************************************************************************/
/************************************************************************************************************/

static void
_print_cl(const char *str)
{
	du_color_t cl;

	if (du_color_from_str(&cl, str)) {
		printf("color : r = %f,\tg = %f,\tb = %f,\ta = %f\n", cl.r, cl.g, cl.b, cl.a);
	} else {
		printf("failed to convert color \'%s\'\n", str);
	}
}
