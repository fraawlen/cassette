#include <stdio.h>

#include <derelict/dr.h>

#include "config.h" /* generated by makefile */

/************************************************************************************************************/
/************************************************************************************************************/
/************************************************************************************************************/

#define _SAMPLE_CONFIG_PATH "/tmp/dr_config_sample"

/************************************************************************************************************/
/************************************************************************************************************/
/************************************************************************************************************/

static void _generate_source (void);
static void _print_resource  (dr_config_t *cfg, const char *namespace, const char *property);

/************************************************************************************************************/
/************************************************************************************************************/
/************************************************************************************************************/

/**
 * In this 1st example, a simple resource look-up is done after loading the configuration.
 */

int
main(void)
{
	dr_config_t *cfg;

	/* init */

	cfg = dr_config_create(0);

	_generate_source();

	/* operations */

	dr_config_push_source(cfg, _SAMPLE_CONFIG_PATH);

	if (!dr_config_load(cfg))
	{
		printf("configuration failed to load\n");
	}

	printf("namespace\tproperty\traw_values\n");
	printf("---------\t--------\t----------\n");

	_print_resource(cfg, "example-1", "values-1");
	_print_resource(cfg, "example-1", "values-2");
	_print_resource(cfg, "example-1", "values-3");
	_print_resource(cfg, "example-1", "whatever"); /* expected to not be found */
	_print_resource(cfg, "example-9", "values-1"); /* expected to not be found */

	/* end */

	if (dr_config_has_failed(cfg))
	{
		printf("configuration has failed during operation.\n");
	}

	dr_config_destroy(&cfg);

	return 0;
}

/************************************************************************************************************/
/* _ ********************************************************************************************************/
/************************************************************************************************************/

static void
_generate_source(void)
{
	FILE *f;

	if (!(f = fopen(_SAMPLE_CONFIG_PATH, "w")))
	{
		printf("sample configuration in %s could not be generated\n", _SAMPLE_CONFIG_PATH);
		return;
	}

	fprintf(f, "%.*s", examples_config_len, examples_config);

	fclose(f);
}

/* - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -*/

static void
_print_resource(dr_config_t *cfg, const char *namespace, const char *property)
{
	printf("%s\t%s", namespace, property);

	dr_resource_fetch(cfg, namespace, property);
	while (dr_resource_pick_next_value(cfg))
	{
		printf("\t%s", dr_resource_convert_to_string(cfg));
	}

	printf("\n");
}
